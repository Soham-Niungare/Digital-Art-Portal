package com.app.model.enums;

public enum ArtworkStatus {
    AVAILABLE,      // Artwork is available for purchase
    SOLD,          // Artwork has been sold
    ON_EXHIBITION, // Artwork is currently being exhibited
    RESERVED,      // Artwork is reserved for a potential buyer
    UNDER_REVIEW,  // New artwork pending approval
    ARCHIVED       // Artwork no longer available for display/sale
}